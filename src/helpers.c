#include "../inc/push_swap.h"

void	ft_print_title(char *title)
{
	if (title)
	{
		ft_putstr_fd("*----------------------------*\n", 1);
		ft_putstr_fd("*-----\t     ", 1);
		ft_putstr_fd(title, 1);
		ft_putstr_fd(" \t-----*\n", 1);
		ft_putstr_fd("*----------------------------*\n", 1);
	}
}
